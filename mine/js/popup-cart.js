function gotoCartModal() {

	var gotoCartMask = document.getElementById("add-gotoCartMask");
	if (gotoCartMask.style.display === "none") {
		gotoCartMask.style.display = "block";
	} else {
		gotoCartMask.style.display = "none";
	};

	var gotoCartPopup = document.getElementById("gotoCartPopup");
	if (gotoCartPopup.style.display === "none") {
		gotoCartPopup.style.display = "block";
	} else {
		gotoCartPopup.style.display = "none";
	};

};









