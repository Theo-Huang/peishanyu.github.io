function lookbookModal() {

	var lookbookMask = document.getElementById("add-lookbookMask");
	if (lookbookMask.style.display === "none") {
		lookbookMask.style.display = "block";
	} else {
		lookbookMask.style.display = "none";
	};

	var lookbookPopup = document.getElementById("lookbookPopup");
	if (lookbookPopup.style.display === "none") {
		lookbookPopup.style.display = "block";
	} else {
		lookbookPopup.style.display = "none";
	};

};









