function previewModal() {

	var previewMask = document.getElementById("add-previewMask");
	if (previewMask.style.display === "none") {
		previewMask.style.display = "block";
	} else {
		previewMask.style.display = "none";
	};

	var previewPopup = document.getElementById("previewPopup");
	if (previewPopup.style.display === "none") {
		previewPopup.style.display = "block";
	} else {
		previewPopup.style.display = "none";
	};

};









