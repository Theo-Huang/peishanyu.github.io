function letteringModal() {

	var letteringMask = document.getElementById("add-letteringMask");
	if (letteringMask.style.display === "none") {
		letteringMask.style.display = "block";
	} else {
		letteringMask.style.display = "none";
	};

	var letteringPopup = document.getElementById("letteringPopup");
	if (letteringPopup.style.display === "none") {
		letteringPopup.style.display = "block";
	} else {
		letteringPopup.style.display = "none";
	};


};









