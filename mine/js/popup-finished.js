function cusFinishedModal() {

	var cusFinishedMask = document.getElementById("add-cusFinishedMask");
	if (cusFinishedMask.style.display === "none") {
		cusFinishedMask.style.display = "block";
	} else {
		cusFinishedMask.style.display = "none";
	};

	var cusFinishedPopup = document.getElementById("cusFinishedPopup");
	if (cusFinishedPopup.style.display === "none") {
		cusFinishedPopup.style.display = "block";
	} else {
		cusFinishedPopup.style.display = "none";
	};

};









